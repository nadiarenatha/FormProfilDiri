    private void UbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UbahActionPerformed
       dispose();
       Form1 form_ = new Form1();
       form_.setVisible(true);
          DefaultTableModel table = (DefaultTableModel)Tabel1.getModel();
        int row = Tabel1.getSelectedRow();
        if(row >= 0){
            table.removeRow(row);
            try {
                File mahasiswa = new File("D:/Mahasiswa.txt");
                if(mahasiswa.exists()){
                    mahasiswa.createNewFile();
                }
                  buffw = new BufferedWriter(new FileWriter(mahasiswa.getAbsoluteFile()));
                  for (int a = 0; a < Tabel1.getRowCount(); a++) {
                      for (int b = 0; b < Tabel1.getColumnCount(); b++) {
                         buffw.write(Tabel1.getModel().getValueAt(a,b)+"#");   
                      }buffw.write("\r\n");
                }
                 buffw.close();
            } catch (Exception e) {
            }
        }     
     dispose();
     form_.setVisible(true);
     form_.update();
    }//GEN-LAST:event_UbahActionPerformed

