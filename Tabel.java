    private void HapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_HapusActionPerformed
        DefaultTableModel table = (DefaultTableModel)Tabel1.getModel();
        int baris = Tabel1.getSelectedRow();
        if(baris >= 0){
            table.removeRow(baris);
            try {
                File mahasiswa = new File("D:/Mahasiswa.txt");
                if(mahasiswa.exists())
                {
                    mahasiswa.createNewFile();
                }
                buffw = new BufferedWriter(new FileWriter(mahasiswa.getAbsoluteFile()));
                for (int i = 0; i < Tabel1.getRowCount(); i++) 
                {
                    for (int j = 0; j < Tabel1.getColumnCount(); j++) 
                    {
                        buffw.write(Tabel1.getModel().getValueAt(i,j)+"#");   
                    }buffw.write("\r\n");
                }
                buffw.close();
            }catch (Exception e)
            {
                
            }
        }
    }//GEN-LAST:event_HapusActionPerformed

